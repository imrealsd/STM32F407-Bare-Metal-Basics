#include "gpio.h"

/*Data to be sent on uart [2 half words from memory]*/
uint16_t buff[2] = {0x3030,0x3131};

/*Private function prototype*/
void SystemClock_Config(void);


/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{   
    /*Initialise Drivers*/
    HAL_Init();
    SystemClock_Config();
    MX_GPIO_Init();
    MX_DMA_Init();
    MX_USART1_UART_Init();

    /**
     * DMA Memory to Peripheral Transfer
     * FIFO Mode [FIFO tolal size (4*32) bit = 16 byte]
     * FIFO Thresold = 1/4 = 4 byte
     * FIFO will fire only after 4 byte is full & at the end the remaining data (flush)
     * 
     * PSIZE = 8bit or 1 byte, MSIZE = 16bit or 2 byte
     * Number of Transfer (always respect to PSIZE) will be multiple of (16/8) = 2 [to empty fifo completely] 
     * Setting Number of transfer =  4 [4 bytes = 2 half words]
    */

    HAL_UART_Transmit_DMA(&huart1, (uint16_t *)buff, 4);
    
    while (1) {}
    return 0;
}



